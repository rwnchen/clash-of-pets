                <h2>Thinking about a cat?</h2>
                
                <p>Worshipped by both the Egyptians and the internet, cats have a certain appeal that dogs don't have. Maybe we're attracted to emotionally unavailable pets. Maybe it's the feeling of breaking through their cold, apathetic exteriors to reach their furry little hearts. Maybe we just like seeing these graceful hunters <a href="https://www.youtube.com/watch?v=ny5vGbTfB8c">fail miserably</a>.</p>
                
                <p>Regardless of the reasons why we love cats, there are a number of pros and cons to owning one:</p>
                
                
                <!-- text taken from https://pethelpful.com/cats/Pros-and-Cons-of-Cats-and-Dogs -->
                
                <h3>PROS</h3>

                <p>Cats are easy to maintain. All they need is a food and water bowl, a litter box and they should be good to go. Additional items can be added at your own free choice. Some spoiled cats have cat trees, scratching posts, toys, catnip, special shampoos and grooming kits. Others simply enjoy a window view and a piece of paper rolled up and that will make their day.</p>

                <p>Cats are independent. Yes, you can go to work for 8 hours straight and your cat may not miss you or at least it will not show much commotion when you leave. They can easily entertain themselves and cater to their needs on their own. I once, had to leave my cat for the weekend alone (boarding was out of question as she did not do well in unfamiliar territory) and I was sick worried and when I came back there she was like if nothing happened.</p>

                <p>Cats are clean. You will not smell that typical doggy odor and they rarely have anal gland issues. Your home will not smell like cat and if you are particularly careful in keeping the litter box clean and fresh with high quality litter chances are, nobody will ever now you share your roof with a feline.</p>

                <p>Cats are lazy. Well, some are really not much. There are some hyperactive breeds such as Bengals and Siamese, but most cats are pretty lazy pillow warmers that sleep up to 16 hours a day. No need to walk them every day to keep their energy levels low, a ball tossed about the room will suffix.</p>

                <p>Cats are litter box trained. Cats do not need to be walked at 5 in the morning in the dead of the winter with Subarctic temperatures. They will do their business in their litter box while you can sleep in after a late evening. All they need is a litter and a litter box and a 5 second daily scoop.</p>
                
                <h3>CONS</h3>

                <p>Cats are standoffish. While this may be true, I have encountered a few individuals that were sort of "doggy cats" really eager to stay near their owners and happy as can be to greet them. However, the majority of cats I have owned decided when to give and get affection on their own terms. They also decide when they had enough, and if you own a cat with bad temper he/she may let you know with some teeth marks.</p>

                <p>Cats have nails. Yes, those claws can do their fair share of damage to your Lazy boy sofa and recliner. If not redirected to a nice scratching post, your home may be quickly destroyed. While declawing should be out of question, luckily, frequent nail trimmings may save some of your furniture and upholstery.</p>

                <p>Cats do not like to travel. Cats are very territorial, they feel calm when they are in their territory and their scent is over their favorite objects and people. If you plan to travel a lot or go on vacation, your cat may not be a happy camper. However, there are always exclusions and some cats that traveled a lot as kittens are quite comfortable being moved about cross country.</p>

                <p>Cats may cause allergies. Their fur carries dander and their saliva is a known culprit in causing allergies. There are a lot of people suffering from allergies. If you are allergic to cats but do not care much about appearance, a hairless cat breed like the Sphinx or Devon Rex may be a solution.</p>

                <p>Cats are not too easy to train. While your dog may go to obedience classes, your cat may have a hard time learning things like walking politely on a leash. It is hard to tell if the cat is really acting dumb like he has no clue what you want or if it is just a smart strategy to make humans give up on training them. Telling a cat not to climb on your kitchen counter or not to steal your steak from the table may be a challenge that will require some good thinking and special strategies.</p>
                
